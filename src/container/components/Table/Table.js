export default function Table() {
  const column_name = ['checkbox', 'user name', 'eamil']; // 테이블 header
  const [tableData, setTableData] = useState(data); // 데이터
  const [selectRowId, setSelectRowId] = useState([]); // 선택된 row의 id

  // 전체 체크박스 선택
  const selectAllRow = useCallback(() => {
    setSelectRowId((prev) => {
      if (selectRowId.length === tableData.length) {
        // 이미 모든 행이 선택된 상태라면 모두 해제
        return [];
      } else {
        return tableData.map((it) => it.id);
      }
    });
  }, [tableData, selectRowId]);

  const changeCheckbox = useCallback((rowId) => {
    setSelectRowId((prev) => {
      if (prev.includes(rowId)) {
        // 이미 선택된 상태라면 선택 해제
        return prev.filter((id) => id !== rowId);
      } else {
        // 선택되지 않은 상태라면 선택
        return [...prev, rowId];
      }
    });
  }, []);

  // pagenation setting
  const [currentPage, setCurrentPage] = useState(1); // 현재 페이지
  const [rowsPerPage, setRowsPerPage] = useState(5); // 한 페이지에서 보여줄 row 수
  const [pagenationSize, setPagenationSize] = useState(5); // pagenation 갯수
  const [totalCount, setTotalCount] = useState(tableData.length); // 전체 갯수
  const handlePageChange = (page) => {
    setCurrentPage(page);
  };

  useEffect(() => {
    // 페이지 이동에 따라 테이블 데이터 업데이트
    const startIndex = (currentPage - 1) * rowsPerPage;
    const endIndex = startIndex + rowsPerPage;
    const newData = data.slice(startIndex, endIndex);
    setTableData(newData);
    // totalCount 상태 업데이트
    setTotalCount(data.length);
    // 페이지 이동 시 전체 선택 해제
    setSelectRowId([]);
  }, [currentPage, rowsPerPage]);

  return (
    <div className={styles.tableWrap}>
      <table className={styles.table}>
        <thead>
          <tr>
            {column_name.map((it, id) => (
              <th key={`thead_${id}`}>
                {it === 'checkbox' ? (
                  <input
                    type="checkbox"
                    checked={selectRowId.length === tableData.length}
                    onChange={() => selectAllRow()}
                  />
                ) : (
                  it
                )}
              </th>
            ))}
          </tr>
        </thead>
        <tbody>
          {tableData.map((it, id) => (
            <tr key={`tbody_${id}`}>
              <td className={styles['ta-c']}>
                <input
                  type="checkbox"
                  id={it.id}
                  checked={selectRowId.includes(it.id)}
                  onChange={() => changeCheckbox(it.id)}
                />
              </td>
              <td className={styles['ta-c']}>{it.name}</td>
              <td>{it.email}</td>
            </tr>
          ))}
        </tbody>
      </table>
      <div className={styles.pagenation}>
        <Pagenation
          currentPage={currentPage}
          rowSize={rowsPerPage}
          pagenationSize={pagenationSize}
          totalCount={totalCount}
          onPageChange={handlePageChange}
        />
      </div>
    </div>
  );
}
